							<h2>Клиновой анкер</h2>
							<p>Востребованной моделью механического крепежа является клиновой анкер — резьбовая шпилька, хвостовик которой имеет конусообразную форму. </p>
							<p>Особенности конструкции — короткий отрезок сцепления с основанием, не позволяют применять ее к неоднородным, пористым структурам. Область использования этого метиза — монолитный бетон.</p>
							<ul>
								<li >Клиновой анкер имеет унифицированную маркировку — WAM, дополненную цифровым кодом, описывающим типоразмер изделия. </li>
							</ul>
							<p>По ГОСТу его минимальная длина начинается от 40 мм, увеличиваясь с шагом в 5 мм.</p>
