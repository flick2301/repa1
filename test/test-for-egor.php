							<p>Демонтаж крепления для повторного использования исключен. Оптимальный способ разборки соединения — обрезка шляпки анкер-клина «болгаркой». </p>
							<h2>Забивной анкер</h2>
							<p>Быстрый монтаж с возможностью многократного крепления на стену грузов среднего и большего веса обеспечивают забивные анкеры. Приоритетная область их применения — разнообразные подвесные системы. Основание для установки — полнотелый кирпич, нерастянутые зоны бетона, натуральный камень.</p>
							<p>Большинство моделей на рынке изготовлены из прочной конструкционной стали и имеют защитное анодированное цинковое или латунное покрытие, которое надежно защищает их от коррозии.</p>
							<p>Забивные анкеры. полностью сделанные из латуни, отличаются пластичностью и коррозионной стойкостью. Это делает их монтаж более удобным и увеличивает срок эксплуатации, но несколько снижает прочностные параметры, в сравнении с крепежом из стали.</p>
							<h2>Конструкция</h2>
							<p>В производстве забивных анкеров не используется сварка и пайка, это увеличивает прочность крепления. Состоит он из двух элементов: разрезной пружинящей втулки (цанги) и распорного элемента — клина.</p>
							<ul>
								<li>Внутренняя сторона втулки имеет метрическую резьбу для вкручивания болтов и шпилек.</li>
							</ul>
							<p>На отдельных моделях можно встретить насечку с внешней стороны втулки и клина. Она служит для лучшего сцепления крепежа с основанием и элементов набора между собой.</p>
							<h2>Как работает забивной анкер </h2>
							<p>В месте предполагаемого крепления бурится отверстие, точно повторяющее диаметр и длину наружной гильзы.</p>
							<ul>
								<li>Малая глубина сверления и простой монтаж, не требующий оборудования и чрезмерных усилий, упрощают установку в труднодоступных местах, например, на потолке.</li>
							</ul>
							<p>Как правильно работать с анкерами этого типа? В очищенное от пыли и бетонной крошки отверстие при помощи молотка заподлицо с поверхностью вбивается разрезная втулка. Затем, используя керн либо специальное приспособление для запрессовки — оправку (она исключает повреждение внутренней резьбы), клин забивают до упора, распирая лепестки и фиксируя втулку.</p>
							<p>Заключительный этап — вкручивание болта или шпильки, которые крепят подвесной элемент. Для относительно хрупких или непроверенных оснований есть смысл контролировать момент затяжки, используя динамометрический ключ.</p>
							<h2>Анкер рамный</h2>
							<p>Надежный и недорогой рамный анкер можно встретить на любом строительном объекте. Разработанный, как основное средство фиксации дверных и оконных коробок, он, тем не менее применяется гораздо шире. </p>
							
