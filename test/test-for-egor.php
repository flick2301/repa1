							<h2>Состав жидкого анкера</h2>
							<p>У разных производителей набор и пропорции компонентов инжекционной массы могут незначительно отличаться. Однако базовый состав чаще всего остается неизменным — смола, отвердитель, наполнитель.</p>
							<ul>
								<li >Период застывания клеящего вещества может составлять от нескольких часов до суток.</li>
							</ul>
							<p>Выбор химического анкера зависит от будущих условий эксплуатации — механических нагрузок, прочности основания, климата.</p>
