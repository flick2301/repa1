							<h2>Конструкция </h2>
							<p>Стандартный клиновой анкер состоит из следующих элементов:</p>
							<ul>
								<li >металлическая шпилька;</li>
								<li >независимая распорная втулка;</li>
								<li >гайка с прижимной шайбой или гайка с юбкой (фланцевая).</li>
							</ul>
							<p>Основным материалом в производстве является сталь, марка и способ защиты которой подбираются в зависимости от параметров эксплуатации крепления.</p>
							<ul>
								<li >Изделия из стали с цинковым покрытием подойдут для внешнего и внутреннего монтажа в условиях отсутствующей или умеренной влажности.</li>
								<li >Нержавеющая сталь марок A2-A4 используется в местах с постоянно высокой влажностью (конструкции, погруженные в воду) и агрессивной среде.</li>
							</ul>
							<p>Стоимость анкеров из нержавеющей стали выше аналогов, поэтому их использование должно быть продуманным и рациональным.</p>
							<h2>Как работает клиновой анкер</h2>
							<p>Понять, как работает клиновой анкер несложно. Изделие плотно вставляется в гнездо и забивается до упора молотком. После этого, на свободный наружный конец шпильки крепится монтируемый элемент и прижимается шайбой с гайкой. </p>
							<p>В момент закручивания гайки происходит вытягивание шпильки с одновременным раскрытием втулки, которая меняя форму и увеличиваясь в диаметре, создает  необходимую прочность сцепления метизов с основанием.</p>
							<h2>Анкер-клин</h2>
							<p>Не то же самое, что клиновой анкер! Имея более простую форму и принцип действия широко применяется для быстрого крепежа периферийных систем небольшого веса — кабельных трасс, вентиляционных каналов, подвесных потолков. </p>
							<figure> 
								<img alt="Пример расчета анкера" title="Пример расчета анкера" src="/upload/medialibrary/2af/2af8cc5fb53a16152e988df018d2a114.jpg">
