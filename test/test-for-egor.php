							<p>С учетом того, как работает химический анкер, область его применения фактически не ограничена. Крепеж активно востребован в любой локации, где ведутся строительные или ремонтные работы. В частности:</p>
							<ol>
								<li >армирование, крепление ответственных бетонных конструкций (мостов, фундаментов, колонн, лестничных пролетов, межэтажных перекрытий);</li>
