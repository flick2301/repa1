								<li >установка сервисного, коммунального оборудования (наружные блоки сплит-систем, спутниковые тарелки, осветительные, сантехнические приборы).</li>
							</ol>
							<p>Технология монтажа и эксплуатационные параметры синтетического состава делают химические анкеры незаменимыми в условиях повышенной влажности, в местах со строгими требованиями к безопасности электроизоляции. </p>
							<h2>Как работает химический анкер </h2>
							<p>Использовать жидкий анкер просто. В основании (стена, пол, потолок) готовится отверстие, диаметр которого на 2-4 мм превышает размер болта. Очищенную от мусора полость заполняют клеевым составом и аккуратно располагают в нем крепеж.</p>
