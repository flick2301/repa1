							<h2>Типы химических анкеров</h2>
							<p>Существует две разновидности: в ампулах и картриджах (инъекционные). Их главное отличие — возможность повторного использования. Если ампульный — это одноразовое решение для единственного крепления, то состав в картриджах позволяет вести последовательный монтаж десятков анкеров без манипуляций с заменой емкости.</p>
							<ul>
								<li >Заявленный срок службы сертифицированного химического анкера — около 50 лет.</li>
							</ul>
							<p>Крепеж в ампулах наиболее эффективен при работе с плотными материалами, тогда как инжекционная масса с возможностью дозированного заполнения, оптимальна для крепления элементов в пористых и пустотелых основаниях.</p>
