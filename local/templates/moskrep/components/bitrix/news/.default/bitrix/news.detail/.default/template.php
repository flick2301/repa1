                     <li>Изделия, фиксируемые за счет силы трения. Типовые крепежи, совместимы с большинством оснований. Метизы используются при монтаже навесного оборудования, инженерных сетей, осветительных приборов.</li>
                     <li>Крепежи на базе упоров. Анкеры с распорками различных типов. Они обладают высокой несущей способностью, работают с гипсокартоном, фанерой и прочими тонкостенными материалами. Метизы позволяют фиксировать тяжелые конструкции, не разрушая опору.</li>
                     <li>Продукция на базе клеевых составов. Химические анкеры для пористых оснований. Изделия устанавливаются в пено- и шлакоблоки, востребованы на производстве и в быту.</li>
