                  <p>Удержание крепежей осуществляется за счет трения, упора или склеивания. Каждое действие обладает собственной спецификой, заслуживает отдельного рассмотрения.</p>
                  <h2>Трение</h2>
                  <p>Сила трения противодействует внешней вытягивающей нагрузке. Она формируется при прилегании распорного элемента к телу опоры.</p>
