                  <h2>Упор</h2>
                  <p>Крепеж упирается во внешнюю или внутреннюю поверхность опоры. Он устанавливается в заранее подготовленном отверстии, удерживается специальными распорками.</p>
                  <p><img src="uploads/simple-article/image-03.jpg" width="572" height="166" alt=""></p>
                  <h2>Склеивание</h2>
                  <p>Фиксация метиза за счет клеевого состава. Как правило, задействуется двухкомпонентная смола, формирующая неразъемный узел.</p>
