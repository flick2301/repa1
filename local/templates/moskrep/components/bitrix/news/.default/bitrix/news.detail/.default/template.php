                  <p>Создание узлов в рамках составных конструкций допускает использование различных типов анкеров.</p>
                  <h2>Подробнее о специфике крепежей</h2>
                  <p>Анкерные крепления различаются конструктивным исполнением, несущей способностью, спецификой монтажа. Ниже мы рассмотрим наиболее распространенные метизы, поговорим об особенностях их использования.</p>
                  <h3>Химические анкеры</h3>
                  <p>Химический анкер – это относительно свежий продукт, представленный в виде клеевой смеси. Состав состоит из синтетической смолы, наполнителя и отвердителя, подходит для частного и производственного использования.</p>
                  <p>Анкер монтируется в определенном порядке:</p>
                  <ol>
                     <li>подготовка отверстия в пористом основании;</li>
                     <li>очистка проема от пыли и грязи;</li>
                     <li>помещение в отверстие рабочей смеси;</li>
                     <li>установка анкерного крепежа.</li>
                  </ol>
                  <p>Состав застывает в течение определенного времени. Сформированный узел обладает значительной прочностью, устойчив к разрушению и извлечению.</p>
                  <p>При производстве клеевой смеси <a href="/">используется полиэстер</a> или акрил. Продукция поставляется в специальных емкостях, защищена от внешнего воздействия.</p>
                  <p>Выделяют два типа химических анкеров.</p>
                  <p><strong>Ампульные.</strong> Клеевые смеси реализуются в стеклянной таре. Ампулы помещаются в монтажный проем, после чего раздавливаются анкером. Высвободившийся состав затвердевает, образуя неразъемное соединение.</p>
                  <p><img src="uploads/simple-article/image-05.jpg" width="872" height="344" alt=""></p>
                  <p><strong>Инъекционные.</strong> Химические компоненты хранятся в картриджах. Для подачи клеевого раствора используется специальный пистолет.</p>
                  <p><img src="uploads/simple-article/image-06.jpg" width="643" height="644" alt=""></p>
                  <p>Химические анкеры совместимы с газосиликатом, известняком, пустотелым кирпичом. Продукция не боится температурных перепадов, востребована при реализации проектов различной сложности.</p>
                  <p>Достоинства монтажных смесей:</p>
                  <ul>
                     <li>формирование соединений в любом пространственном положении;</li>
                     <li>размещение анкеров вблизи друг друга;</li>
                     <li>быстрая подготовка к работе;</li>
                     <li>простой монтаж.</li>
                  </ul>
                  <p>Химические анкеры используются при решении следующих задач:</p>
                  <ul>
                     <li>установка армирующих штырей;</li>
                     <li>проведение кровельных работ;</li>
                     <li>установка удерживающих и декоративных конструкций;</li>
                     <li>проведение ремонтных мероприятий.</li>
                  </ul>
                  <p>Удерживающий состав является диэлектриком, задействуется при электромонтажных работах.</p>
                  <h3>Распорные анкеры</h3>
                  <p>Распорные анкеры – это типовые крепежи, удерживаемые за счет силы трения. Изделия состоят из распорной втулки, фланцевой гайки, шпильки с конусообразным основанием.</p>
                  <p>Верхняя часть метиза представлена в различном исполнении:</p>
                  <ul>
                     <li>кольцо;</li>
                     <li>крюк;</li>
                     <li>костыль;</li>
                     <li>сплющенное ушко.</li>
                  </ul>
                  <p><img src="uploads/simple-article/image-07.jpg" width="872" height="421" alt=""></p>
                  <p>Наиболее распространенные анкеры имеют навершие в виде шестигранной шляпки.</p>
                  <p>Крепежи обозначаются последовательностью трех цифр. Первая отображает наружный диаметр метиза, вторая – диаметр резьбы, третья – общую длину. Маркировка представлена на упаковочном стикере и в сопроводительных документах.</p>
                  <p>Анкеры устанавливаются в заранее подготовленные отверстия. Они протягиваются подходящим ключом, образуют прочное, долговечное соединение.</p>
                  <p>Посредством распорных крепежей монтируются лестничные конструкции, кондиционеры, инженерные сети.</p>
                  <p>Продукция обладает следующими преимуществами:</p>
                  <ul>
                     <li>стойкость к ударному воздействию;</li>
                     <li>длительный срок службы;</li>
                     <li>высокая прочность на излом.</li>
                  </ul>
                  <p>Крепежи не совместимы с пористыми материалами. Они размещаются в бетоне, естественном и искусственном камне.</p>
                  <h3>Упорные крепежи</h3>
                  <p>Упорные анкеры являются аналогами дюбелей Молли. Продукция ориентирована на тонкостенные опоры, имеет типовую конфигурацию:</p>
                  <ul>
                     <li>цанга с продольными прорезями и внутренней резьбой;</li>
                     <li>винт с зазубренной шайбой;</li>
                     <li>опорный стержень.</li>
                  </ul>
                  <p>Метиз подвергается гальваническому цинкованию, обладает длительным сроком службы.</p>
                  <p><img src="uploads/simple-article/image-08.jpg" width="500" height="264" alt=""></p>
                  <p>Анкеры монтируются в следующем порядке:</p>
                  <ul>
                     <li>разметка основания с последующим высверливанием отверстий;</li>
                     <li>установка цанги в рабочий проем;</li>
                     <li>фиксация зазубренной шайбы;</li>
                     <li>частичное вкручивание винта с последующим вытягиванием;</li>
                     <li>формирование распорных элементов вследствие деформации цанги;</li>
                     <li>полное вкручивание винта.</li>
                  </ul>
                  <p>Вновь созданный узел проходит визуальную проверку. При малой несущей способности соединения (неполное раскрытие цанги), крепеж заменяется новым.</p>
                  <p>При подборе метизов учитываются характеристики основания, масса фиксируемых элементов, специфика эксплуатации узла.</p>
